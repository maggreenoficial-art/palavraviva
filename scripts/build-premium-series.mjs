function script({ open, bibleId, bibleLead, practice, prayer, anchor, close, disclaimer }) {
  const parts = [
    open,
    `<break time="2.5s" />`,
    bibleLead,
    `<break time="1.5s" />`,
    `{{BIBLE:${bibleId}}}`,
    `<break time="3.5s" />`,
    practice,
    `<break time="2.5s" />`,
    `Vamos orar.`,
    `<break time="2.0s" />`,
    prayer,
    `<break time="3.0s" />`,
    `Frase-âncora: ${anchor}`,
    `<break time="2.5s" />`,
    close,
  ];
  if (disclaimer) {
    parts.push(
      `<break time="2.0s" />`,
      `Este áudio oferece apoio espiritual e não substitui terapia ou acompanhamento médico.`,
    );
  }
  return parts.join('\n');
}

